<h3>3. Employment State Insurance Scheme (ESIS) | Overview :</h3> 
<p>It is a social security scheme that takes care of the worker class.  There is a huge worker category of persons who are engaged in different establishments such as factories etc. who are always under the threat of health hazards and injury. These people get their coverage benefit along with that of their dependents. If the workers in a factory are more than 9, ESI becomes applicable for the workers. Apart from the factories, the workers in other establishments such as shops, hotels, cinema halls, transport sector employees are all covered under ESI. The workers in these establishments have to be 20 persons minimum for the coverage to be binding. Staffs working in the educational institutes are also covered by ESI. All areas in the country are not yet covered by ESI. Gradually, the areas are being increased.<p> 

<h3>Documents Required :</h3>

<p>The person should possess the ESI card to avail the facility.<p>

<h3>Benefits :</h3>

<p>If a person becomes sick, they are eligible to get cash benefits by availing the treatment at the notified ESI hospitals. If a person undergoes temporary or permanent disability, he will get the benefit from it. If a person becomes incapacitated, he will get a pension monthly to run his household expenses. The same will be given to the family members, if the person dies due to any kind of injury during the course of employment.<p>
