<h3>2. Central Government Health Scheme (CGHS) | Overview :</h3> 
<P>The CGHS Scheme is devised for the employees under Central Government services. It has a wide range of benefits for the employees. The benefits can be enjoyed by the current working people and also the ones who have retired from the Central Government Services.  Their dependents are also eligible to enjoy the similar benefits.  The facilities that are available under this scheme are available in all the state capitals along with the major cities and towns of India. Full-fledged health services are admissible under this Health Scheme.<p>

<h3>Documents Required :</h3>

<p>The patient must have the authentic document to prove that he is a Central Government employee (working or retired).<P>

<h3>Benefits :</h3>

<h3>The major areas of the benefits that can be enjoyed under this scheme are the dispensary serves inclusive of the domiciliary state, consultation with the specialist physicians; all types of pathological and radiological investigations, hospitalization charges, arrangement of the medicines and health education for the patients.</h3>
