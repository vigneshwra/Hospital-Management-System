<h3>4. Aam Aadmi Bima Yojana (AABY) and Janashree Bima Yojana (JBY) | Overview :</h3>
 <p>The beneficiary of the AABY scheme has to belong to landless persons in the rural areas. A single member of the family gets the benefit of this health scheme. The wage earner of the family is the person who gets the benefit of this scheme. The person has to be with the age of 18 to 59 to qualify. The premium is paid by the Centre and the State equally. The coverage is for people under the BPL category or slightly above the limits for the JBY Scheme. Both these schemes have now been combined.<p>

<h3>Documents Required :</h3>

<P>For AABY scheme, should prove that the person belongs to the rural area and does not possess any land
For JBY scheme, should prove the income figures as being below or slightly above BPL category <P>
<h3>Benefits :</h3>

<p>There are benefits available on the natural as well as the accidental death of the person covered. Partial or permanent disability is also covered in this scheme. Meritorious students of these families are eligible to get scholarships from this scheme. Similar benefits are permissible to people from 45 specified types of occupation under the JBY Scheme. The benefits are similar to the AABY Scheme. The age eligibility is also the same.<p>
